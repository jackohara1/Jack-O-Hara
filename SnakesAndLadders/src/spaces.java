
public class spaces {


    private int xAxis;
    private int yAxis;












    public spaces(int xAxis,int yAxis){
        setXAxis(xAxis);
        setYAxis(yAxis);
    }



    public int getXAxis() {
        return xAxis;
    }

    public int getYAxis(){
        return yAxis;
    }





    public void setXAxis(int xAxis) { this.xAxis = xAxis;}

    public void setYAxis(int yAxis) {
        this.yAxis = yAxis;
    }



}

